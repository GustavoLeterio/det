import { Item, Nutrients } from "../src/Store/Slices/Home/IHome";

export function getNutrientKeyNameFromValue(value: Nutrients): {
  protein: Item[];
  carbohidrate: Item[];
  fiber: Item[];
  fat: Item[];
} {
  const name = Object.keys(Nutrients)[Object.values(Nutrients).indexOf(value)];
  if (name == "protein") return "protein";
  if (name == "fiber") return "fiber";
  if (name == "fat") return "fat";
  return "carbohidrate";
}
